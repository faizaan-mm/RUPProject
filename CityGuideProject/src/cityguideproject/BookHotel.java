    private void updateTable(){
        try{
            String query = " select id,name as Name,address as Address,description as Description, capacity as Capacity from public_spaces where category = 'accomodation' ";
            PreparedStatement pst = connect.prepareStatement(query);
            ResultSet rs = pst.executeQuery();
            users_table.setModel(DbUtils.resultSetToTableModel(rs));
        }
        catch(Exception e){
            System.out.println(e);
        }
    }
