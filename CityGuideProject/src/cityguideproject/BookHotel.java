    private void fillCombo(){
        try{
            String query = " select * from public_spaces where public_spaces.category='accomodation' and id not in (select hotelid from AccomodationStatus where Userid = ? and Status <> 'Request Rejected') ";
            PreparedStatement pst = connect.prepareStatement(query);
            pst.setInt(1, login.user);
            ResultSet rs = pst.executeQuery();
            while(rs.next()){
                String id = rs.getString("id");
                users1.addItem(id);
            }
        }
        catch(Exception e){
            System.out.println(e);
        }
    }
